package ch2;

import java.util.Date;

public class VarTypeEx {
    public static void main(String[] args) {
        // 기본형 (primitive type)
        boolean bool = true;        // 1byte
        char chr = 'a';             // 2byte
        byte bt = 3;                // 1byte
        short sht = 1;              // 2byte
        int it = 1;                 // 4byte
        long lng = 1;               // 8byte
        float flt = 1.0f;    // 4byte
        double dbl = 2.0;           // 8byte

        // 참조형 (reference type)
        Date today = new Date();

    }
}
